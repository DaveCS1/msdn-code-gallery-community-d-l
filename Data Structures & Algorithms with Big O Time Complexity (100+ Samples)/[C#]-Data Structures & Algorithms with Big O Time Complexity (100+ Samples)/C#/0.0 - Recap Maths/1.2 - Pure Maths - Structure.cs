    4. Logarithms

    How many of one number do we multiply to get another number?        (Exponent says how many times to use the number in a multiplication_

    Example: How many 2s do we multiply to get 8?
    Answer: 2 × 2 × 2 = 8, so we needed to multiply 3 of the 2s to get 8
    So the logarithm is 3

    We write "the number of 2s we need to multiply to get 8 is 3" as:   log2(8) = 3

    The number we are multiplying is called the "base", so we can say: 
    • "The logarithm of 8 with base 2 is 3"
    • or "log base 2 of 8 is 3" 
    • or "the base-2 log of 8 is 3"

    In other way, The logarithm tells us what the exponent is!  In that example the "base" is 2 and the "exponent" is 3:
    2^3 = 8

    What exponent do we need (for one number to become another number) ?
    What is log10(100) ... ? 10^22 = 100
    So an exponent of 2 is needed to make 10 into 100, and: log10(100) = 2

