        2. Exponents :

            The exponent of a number says how many times to use the number in a multiplication.
            Exponents are also called Powers or Indices.
            In 8^2 the "2" says to use 8 twice in a multiplication, so 8^2 = 8 × 8 = 64

            Exponents make it easier to write and use many multiplications
            2^4 easier than writing 2 × 2 × 2 × 2

            A negative exponent means how many times to divide one by the number. 
            Example: 8^-1 = 1 ÷ 8 = 0.125
            Example: 5^-3 = 1 ÷ 5 ÷ 5 ÷ 5 = 0.008

            But that can be done an easier way:
            5^-3 could also be calculated like:	1 ÷ (5 × 5 × 5) = 1/53 = 1/125 = 0.008 

            Exponent 1 and 0: 2^1 = 2, 2^0 = 1
            0^0 ? It could be either 1 or 0, and so people say it is "indeterminate"
           
            a^b X a^c  =  a^b+c   The multiplication rule applies just to the product, not to the sum of two numbers.
            a^b / a^c  =  a^b-c
            a^b + a^c  <> a^b+c
            (a^b)^c    =  a^bc
            (ab)^c     =  a^c X b^c
            (a/b)^c    =  a^c / b^c

            Laws of Exponents :

            x^1  = x    6^1  = 6 
            x^0  = 1    7^0  = 1 
            x^-1 = 1/x  4^-1 = 1/4 
  
            x^mx^n  = x^(m+n)  x^2x^3  = x^(2+3) = x^5 
            x^m/x^n = x^(m-n)  x^6/x^2 = x^(6-2) = x^4 
            (x^m)^n = x^(mn)   (x^2)^3 = x^(2×3) = x^6 
            (xy)^n  = x^ny^n   (xy)^3  = x^3y^3 
            (x/y)^n = x^n/y^n  (x/y)^2 = x^2 / y^2 

            √x = x^(1/2) : x greater than or equal to 0.

